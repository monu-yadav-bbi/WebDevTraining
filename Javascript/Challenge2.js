console.log(BMIMark, BMIJohn);

if (BMIMark > BMIJohn) {
  console.log(`Mark's BMI ${BMIMark} is higher than john's ${BMIJohn}!`);
} else {
  console.log(`John's BMI ${BMIJohn} is heigher than Mark's ${BMIMark}!`);
}
